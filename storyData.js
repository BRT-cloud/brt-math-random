function getJosa(name, type) {
  const lastChar = name.charCodeAt(name.length - 1);
  const hasJong = (lastChar - 0xac00) % 28 > 0; // 받침이 있는지 확인
  
  if (type === '은' || type === '는' || type === '이' || type === '가') {
    if (type === '은' || type === '는') {
      return hasJong ? `${name}은` : `${name}는`;
    } else {
      return hasJong ? `${name}이` : `${name}가`;
    }
  }
  if (type === '을' || type === '를') {
    return hasJong ? `${name}을` : `${name}를`;
  }
  if (type === '와' || type === '과') {
    return hasJong ? `${name}과` : `${name}와`;
  }
  return name;
}
